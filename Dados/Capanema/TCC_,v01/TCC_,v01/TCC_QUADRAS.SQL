-- 2. (Opcional) Cria um índice espacial para acelerar buscas por localização
-- Nota: Para índices espaciais no MariaDB, a coluna deve ser NOT NULL
CREATE SPATIAL INDEX sp_index_centroide ON TCC_QUADRAS(centroide);

UPDATE TCC_QUADRAS 
SET centroide = POINT(LNG_CENTROIDE, LAT_CENTROIDE)
WHERE LNG_CENTROIDE IS NOT NULL AND LAT_CENTROIDE IS NOT NULL;

CREATE TABLE ANALISE_ERRO_PROXIMIDADE_DUPLO (
    ID INT AUTO_INCREMENT PRIMARY KEY,
    CODIGO_CARTOGRAFICO VARCHAR(50),
    CI_ID_FACE VARCHAR(100),
    GEO_PONTO POINT,             -- Centroide original (Verde no HTML)
    OSM_VERTICE_1 POINT,         -- Vértice mais próximo (Rank 1 - Vermelho)
    DISTANCIA_1 DOUBLE,          -- Distância para o vértice 1
    OSM_VERTICE_2 POINT,         -- Segundo vértice mais próximo (Rank 2)
    DISTANCIA_2 DOUBLE           -- Distância para o vértice 2
);

    
SELECT 
    P.CODIGO_CARTOGRAFICO,
    P.CI_ID_FACE,
    ST_AsText(P.OSM_VERTICE_1) AS OSM_VERTICE_1,
    ST_AsText(P.OSM_VERTICE_2) AS OSM_VERTICE_2,
    T.SC_ID_LOGRADOURO,
    ST_AsText(F.CENTROIDE) AS CENTROIDE,
    F.ORDEM_FACE,
    T.DIMENSAO_M AS DIM_FACE
FROM 
    TCC_QUADRAS T
JOIN 
    SC_FACES F ON (T.CI_ID_FACE = F.CI_ID_FACE)
JOIN 
    ANALISE_ERRO_PROXIMIDADE_DUPLO P ON (T.CI_ID_FACE = P.CI_ID_FACE);
    
    
